local ListView = require('ui/listview')

--------------------------------------------------------------------------------
local MainMenu

--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
local Ui = {}

--------------------------------------------------------------------------------
function Ui.Create()
    MainMenu = ListView:ListView()
end

--------------------------------------------------------------------------------
function Ui.Destory()
    MainMenu:Hide()
    MainMenu:Destory()
end

--------------------------------------------------------------------------------
function Ui.Clear()
    MainMenu:Hide()
    MainMenu:Clear()
end

--------------------------------------------------------------------------------
function Ui.AppendItem(text)
    MainMenu:AppendItem(text)
    MainMenu:Show()
end

return Ui